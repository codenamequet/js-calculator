var calculator = {

add: function (num1, num2) {
    return num1 + num2
},

subtract: function (num1, num2) {
    return num1 - num2
},

multiply: function (num1, num2) {
    return num1 * num2
},

divide: function (num1, num2) {
    return num1 / num2
},

square: function (num1) {
    return num1 * num1
},

sqrt: function (num1) {
    return Math.sqrt(num1)
},

tip: function (num1) {
    return num1 * .2
}
}
